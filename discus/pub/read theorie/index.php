  <script>
    function canvas() {
      let x = document.querySelector('.chapters');
      x.style.display = "block";
      let y = document.querySelector('.btn');
      y.setAttribute("onclick", "hideOffcanvas()");
    }

    function hideOffcanvas() {
      let x = document.querySelector('.chapters');
      x.style.display = "none";
      let y = document.querySelector('.btn');
      y.setAttribute("onclick", "canvas()");
    }

    function redheart() {
      let redheart = document.getElementById('redheart').style.color = "red";
      let likeButton = document.querySelector('.likeButton');
      likeButton.setAttribute("onclick", "hideheart()");
    }
    function hideheart() {
      let redheart = document.getElementById('redheart').style.color = "black";
      let likeButton = document.querySelector('.likeButton');
      likeButton.setAttribute("onclick", "redheart()");
    }


    function yellowfav() {
      let favorite = document.getElementById('favorite').style.color = "yellow";
      let favButton = document.querySelector('.favButton');
      favButton.setAttribute("onclick", "hidefav()");
    }
    function hidefav() {
      let favorite = document.getElementById('favorite').style.color = "black";
      let favButton = document.querySelector('.favButton');
      favButton.setAttribute("onclick", "yellowfav()");
    }

    function answer(event) {
      const comment = event.target.closest('.comment2');
      const reply = comment.querySelector('.reply');
      reply.style.display = 'block';
      reply.scrollIntoView({ behavior: 'smooth' });
    }

    function love(event) {
      let lovediv = event.target.closest('.onecomment');
      let love = lovediv.querySelector('#love-comment');
      if (love.style.color === "red") {
        love.style.color = "black";
        document.querySelector('.likeCommentButton').setAttribute("onclick", "love(event)");
      } else {
        love.style.color = "red";
      }
    }




  </script>


